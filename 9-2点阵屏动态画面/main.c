#include <STC89C5xRC.H>

#include "MatrixLED.h"

unsigned char Animation[]={
	0xFF,0x08,0x08,0x08,0xFF,0x00,0x00,0x0E,
	0x15,0x15,0x15,0x08,0x00,0x00,0xFF,0x01,
	0x02,0x00,0x00,0xFF,0x01,0x02,0x00,0x00,
	0x0E,0x11,0x11,0x0E,0x00,0x00,0xF9,0x00,
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00
};

void main()
{
	unsigned char i,offset,count=0;
	void MatrixLED_Init();
	while(1)
	{
		
		for(i=0;i<8;i++)
		{
			MatrixLED_ShowColumn(i,Animation[i+offset]);	
		}
		count=count+1;
		if(count>20)
		{
			count=0;
			offset=offset+1;
			if(offset>32)
			{
				offset=0;
			}
		}
		
	}
}
